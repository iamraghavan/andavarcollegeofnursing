    <h3>Maternity and Child Health Nursing Lab</h3>
    <p>The Maternity and Child Health (MCH) Nursing Lab at Andavar College is a specialized facility designed to provide hands-on training and practical experience in the field of maternity and pediatric nursing.</p>
    <p>Equipped with advanced simulation models and state-of-the-art equipment, the MCH Lab offers a comprehensive learning environment for students to develop essential nursing skills in maternal and child healthcare.</p>
    <p>In the MCH Lab, students have access to a range of resources including maternity examination models, gynecological examination models, delivery models, charts, and other equipment. These resources allow students to practice various procedures and techniques related to prenatal care, childbirth, and postnatal care under the guidance of experienced faculty.</p>
    <p>The Child Health Lab within the facility is dedicated to pediatric nursing education. It features models, charts, and various play therapy materials aimed at providing students with a holistic understanding of pediatric healthcare.</p>
    <p>Here, students learn about all the nursing care skills essential for the care of newborns, infants, and children. Through hands-on practice and simulation exercises, students develop proficiency in pediatric assessment, immunization, growth monitoring, and management of common childhood illnesses.</p>
