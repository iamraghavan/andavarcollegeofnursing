    <h3>Nursing Foundations Lab</h3>
    <p>The Nursing Foundations Lab at Andavar College of Nursing provides students with a simulated clinical environment to develop essential clinical skills required for nursing practice. This lab serves as a crucial learning space where students can apply theoretical knowledge to practical scenarios under the guidance of experienced faculty.</p>
    <p>In the Nursing Foundations Lab, students learn and practice basic clinical skills such as patient assessment, vital signs monitoring, medication administration, wound care, and infection control. Through hands-on experience and simulation-based learning, students gain confidence and proficiency in these fundamental nursing tasks.</p>
    <p>The lab is equipped with state-of-the-art medical equipment and simulation manikins that mimic real patient scenarios. This immersive learning environment allows students to enhance their critical thinking, decision-making, and communication skills, preparing them for the challenges of real-world nursing practice.</p>
